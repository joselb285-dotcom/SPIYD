  function getLang() { return localStorage.getItem('spiyd-lang') || 'es'; }

  function setLang(lang) {
    localStorage.setItem('spiyd-lang', lang);
    document.documentElement.lang = lang;
    applyI18n();
  }

  function applyI18n() {
    var lang = getLang();
    var t = TRANSLATIONS[lang] || TRANSLATIONS.es;

    document.querySelectorAll('[data-i18n]').forEach(function (el) {
      var key = el.getAttribute('data-i18n');
      if (t[key] !== undefined) el.textContent = t[key];
    });

    document.querySelectorAll('[data-i18n-placeholder]').forEach(function (el) {
      var key = el.getAttribute('data-i18n-placeholder');
      if (t[key] !== undefined) el.placeholder = t[key];
    });

    document.querySelectorAll('[data-i18n-value]').forEach(function (el) {
      var key = el.getAttribute('data-i18n-value');
      if (t[key] !== undefined) el.value = t[key];
    });

    document.querySelectorAll('.spiyd-lang-btn').forEach(function (btn) {
      var active = btn.dataset.lang === lang;
      btn.style.opacity = active ? '1' : '0.35';
      btn.style.fontWeight = active ? '700' : '400';
      btn.style.textDecoration = active ? 'underline' : 'none';
    });

    document.documentElement.lang = lang;
  }

  window.spiydI18n = {
    getLang: getLang,
    setLang: setLang,
    applyI18n: applyI18n,
    t: function (key) {
      var lang = getLang();
      return (TRANSLATIONS[lang] || TRANSLATIONS.es)[key] || key;
    }
  };

  if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', applyI18n);
  } else {
    applyI18n();
  }
